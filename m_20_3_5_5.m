ixx=imread('clp.png');
ix=rgb2gray(ixx);
i=double(ix);
w1=[0,1,0;1,-4,1;0,1,0];
w2=[1,1,1;1,-8,1;1,1,1];
w3=-w1;
w4=-w2;
subplot(2,3,1);
imshow(ix);
title('original');
subplot(2,3,2);
a=imfilter(i,w1);
imshow(a);
title('w1=[0 1 0;1 -4 1;0 1 0]');
subplot(2,3,3);
b=imfilter(i,w2);
imshow(b);
title('w2=[1 1 1;1 -8 1;1 1 1]');
subplot(2,3,5);
c=imfilter(i,w3);
imshow(c);
title('-w1');
subplot(2,3,6);
c=imfilter(i,w4);
imshow(c);
title('-w2');
