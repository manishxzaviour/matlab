ixx=imread('clpsm2.png');
ix=rgb2gray(ixx);
i=double(ix);
f=fspecial('laplacian');
subplot(2,2,1);
imshow(ix);
title('original');
xlabel('f=laplacian');
subplot(2,2,2);
a=imfilter(i,f,'replicate');
imshow(a);
title('replicate');
subplot(2,2,3);
b=imfilter(i,f,'symmetric');
imshow(b);
title('symmetric');
subplot(2,2,4);
c=imfilter(i,f,'circular');
imshow(c);
title('circular');
