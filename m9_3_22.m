a1=imread('clp.png');
a=rgb2gray(a1);
subplot(2,3,1);
imshow(a);
h1=fspecial('laplacian');
b=imfilter(a,h1);
subplot(2,3,2);
imshow(b);
c=imcomplement(b);
subplot(2,3,3);
imshow(c);
h2=fspecial('laplacian');
d=imfilter(c,h2);
subplot(2,3,4);
imshow(d);
e=c-d;
subplot(2,3,5);
imshow(e);